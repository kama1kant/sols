export const Grid = {
    half:'6',
    full: '12',
    quarter: '4',
    triple: '3',
    double: '2',
    single: '1'
};


class Store {
    currentFeature = 1
    setFeature(feature) {
        this.currentFeature = feature;
    }
}

export const GetGrid = (gridId) => {
    if (gridId == Grid.single) {
        return 'col-sm-1 col-md-1';
    } else if (gridId == Grid.double) {
        return 'col-sm-2 col-md-2';
    } else if (gridId == Grid.triple) {
        return 'col-sm-3 col-md-3';
    } else if (gridId == Grid.quarter) {
        return 'col-sm-4 col-md-4';
    } else if (gridId == Grid.half) {
        return 'col-sm-6 col-md-6';
    } else {
        return 'col-sm-12 col-md-12';
    }
}

